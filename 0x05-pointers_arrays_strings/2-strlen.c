/**
 * @s:string
 * _strlen: returns the length of a string.
 * 
 * description - function that returns the length of a string.
 * return: always(0). 
 */

int _strlen(char *s)
{
    int l;

for (l = 0; s[l] != '\0'; l++)
	{
		continue;
	}

	return (l);
}