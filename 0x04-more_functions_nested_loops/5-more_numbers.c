#include "main.h"

/**
 * more_numbers - more numbers
 *
 * Description: prints 10 times the numbers, from 0 to 14
 * Return: Always (0)
 */

void more_numbers(void)
{
int i = 0, d;
while (/* condition */i <= 10)
{
    /* code */
d = 0;
while (/* condition */d <= 14)
{
    /* code */
_putchar(d);
d++;
}
_putchar("\n");
i++;
}

}
