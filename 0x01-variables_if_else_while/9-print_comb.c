#include <stdio.h>

/**
 * main - Print single digit numbers in the formart
 *
 * Description: Sepaeated by commas
 * Return: Always(0) Success
 */

int main(void)
{
	int i;

	for (i = 48; i <= 56; i++)
	{
		putchar(i);
		putchar(',');
		putchar(' ');
	}
	if (/* condition */ i = 57)
	{
		/* code */
		putchar(i);
	}
	
	putchar('\n');

	return (0);
}
