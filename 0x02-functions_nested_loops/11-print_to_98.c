#include "main.h"

/**
 * print_to_98 - prints natural digits
 * @n: first parameter
 *
 * Description: natural digits
 *
 */

void print_to_98(int n)
{
    n = 0;
    while (/* condition */n <= 98)
    {
        /* code */
        _putchar(n);
        _putchar(',');
        _putchar(' ');
        n++;
    }

}