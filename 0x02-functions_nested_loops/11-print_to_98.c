/**
 * print_last_digit - prints the lest digit
 * @n: first parameter
 *
 * Description: prints the last digit
 * Return: Always (0).
 */

void print_to_98(int n)
{
    n = 0;
    while (/* condition */n <= 98)
    {
        /* code */
        _putchar(n);
        _putchar(',');
        _putchar(' ');
    }

}